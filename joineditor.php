    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const form = document.getElementById('editorApplication');
            const fileInput = document.getElementById('cv');
            const maxFileSize = 5 * 1024 * 1024; // 5MB
            
            // Real-time validation for required fields
            const requiredFields = form.querySelectorAll('[required]');
            requiredFields.forEach(field => {
                field.addEventListener('blur', validateField);
            });
            
            // File size validation
            fileInput.addEventListener('change', function() {
                if (this.files[0] && this.files[0].size > maxFileSize) {
                    showError(this, 'File size exceeds 5MB limit');
                    this.value = ''; // Clear the invalid file
                } else {
                    clearError(this);
                }
            });
            
            // Form submission handler
            form.addEventListener('submit', function(e) {
                e.preventDefault();
                
                let isValid = true;
                
                // Validate all required fields
                requiredFields.forEach(field => {
                    if (!validateField({ target: field })) {
                        isValid = false;
                    }
                });
                
                // Validate file
                if (fileInput.files.length === 0) {
                    showError(fileInput, 'Please upload your CV');
                    isValid = false;
                }
                
                if (isValid) {
                    // Form is valid - show success message (replace with actual submission)
                    alert('Thank you for your application! We will review your submission and get back to you soon.');
                    // form.submit(); // Uncomment to enable actual submission
                } else {
                    // Scroll to first error
                    const firstError = form.querySelector('.is-invalid');
                    if (firstError) {
                        firstError.scrollIntoView({ behavior: 'smooth', block: 'center' });
                    }
                }
            });
            
            // Field validation function
            function validateField(e) {
                const field = e.target;
                const value = field.value.trim();
                
                if (field.required && !value) {
                    showError(field, 'This field is required');
                    return false;
                }
                
                // Email validation
                if (field.type === 'email' && !isValidEmail(value)) {
                    showError(field, 'Please enter a valid email address');
                    return false;
                }
                
                // Textarea minimum length
                if (field.tagName === 'TEXTAREA' && value.length < 50) {
                    showError(field, 'Please provide at least 50 characters');
                    return false;
                }
                
                // Text input minimum length
                if (field.type === 'text' && field.minLength && value.length < field.minLength) {
                    showError(field, `Please provide at least ${field.minLength} characters`);
                    return false;
                }
                
                clearError(field);
                return true;
            }
            
            // Helper functions
            function showError(field, message) {
                clearError(field); // Clear any existing error
                
                const errorDiv = field.parentNode.querySelector('.invalid-feedback');
                if (errorDiv) {
                    errorDiv.textContent = message;
                }
                
                field.classList.add('is-invalid');
            }
            
            function clearError(field) {
                field.classList.remove('is-invalid');
            }
            
            function isValidEmail(email) {
                return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
            }
            
            // Smooth scrolling for anchor links
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function(e) {
                    e.preventDefault();
                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth'
                        });
                    }
                });
            });
        });
    </script>
